#include "LR.h"

int main() {
	LR lr;
	string in;
	while(cin >> in && in != "#")
		lr.add(in);
	in = "";
	cin >> in;
	if(in == "#" || in.size() == 0) return -1;
	lr.loadStr(in);
	lr.build();
	lr.generateDot();
}
